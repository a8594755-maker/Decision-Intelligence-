-- Dataset profile persistence for upload context reuse
-- Step 1 MVP: fingerprint + profile_json + contract_json

DO $$
DECLARE
  user_files_id_type TEXT;
BEGIN
  SELECT data_type INTO user_files_id_type
  FROM information_schema.columns
  WHERE table_schema = 'public'
    AND table_name = 'user_files'
    AND column_name = 'id';

  IF user_files_id_type IS NULL THEN
    RAISE EXCEPTION 'public.user_files.id not found. Please run base schema migration first.';
  END IF;

  IF user_files_id_type = 'uuid' THEN
    EXECUTE $sql$
      CREATE TABLE IF NOT EXISTS public.di_dataset_profiles (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
        user_file_id UUID REFERENCES public.user_files(id) ON DELETE SET NULL,
        fingerprint TEXT NOT NULL,
        profile_json JSONB NOT NULL,
        contract_json JSONB NOT NULL,
        created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
      )
    $sql$;
  ELSIF user_files_id_type = 'bigint' THEN
    EXECUTE $sql$
      CREATE TABLE IF NOT EXISTS public.di_dataset_profiles (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
        user_file_id BIGINT REFERENCES public.user_files(id) ON DELETE SET NULL,
        fingerprint TEXT NOT NULL,
        profile_json JSONB NOT NULL,
        contract_json JSONB NOT NULL,
        created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
      )
    $sql$;
  ELSE
    RAISE EXCEPTION 'Unsupported public.user_files.id type: %', user_files_id_type;
  END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_di_dataset_profiles_fingerprint
  ON public.di_dataset_profiles(fingerprint);

CREATE INDEX IF NOT EXISTS idx_di_dataset_profiles_user_created
  ON public.di_dataset_profiles(user_id, created_at DESC);

ALTER TABLE public.di_dataset_profiles ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view own dataset profiles" ON public.di_dataset_profiles;
CREATE POLICY "Users can view own dataset profiles"
  ON public.di_dataset_profiles FOR SELECT
  USING (auth.uid() = user_id);

DROP POLICY IF EXISTS "Users can insert own dataset profiles" ON public.di_dataset_profiles;
CREATE POLICY "Users can insert own dataset profiles"
  ON public.di_dataset_profiles FOR INSERT
  WITH CHECK (auth.uid() = user_id);

DROP POLICY IF EXISTS "Users can update own dataset profiles" ON public.di_dataset_profiles;
CREATE POLICY "Users can update own dataset profiles"
  ON public.di_dataset_profiles FOR UPDATE
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

DROP POLICY IF EXISTS "Users can delete own dataset profiles" ON public.di_dataset_profiles;
CREATE POLICY "Users can delete own dataset profiles"
  ON public.di_dataset_profiles FOR DELETE
  USING (auth.uid() = user_id);

-- Ensure PostgREST picks up new table immediately
NOTIFY pgrst, 'reload schema';
